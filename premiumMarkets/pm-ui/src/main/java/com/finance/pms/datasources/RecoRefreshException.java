package com.finance.pms.datasources;

public class RecoRefreshException extends RuntimeException {

	private static final long serialVersionUID = 5182777977058348465L;

	public RecoRefreshException() {
		super();
	}

	public RecoRefreshException(String message, Throwable cause, boolean enableSuppression, boolean writableStackTrace) {
		super(message, cause, enableSuppression, writableStackTrace);
	}

	public RecoRefreshException(String message, Throwable cause) {
		super(message, cause);
	}

	public RecoRefreshException(String message) {
		super(message);
	}

	public RecoRefreshException(Throwable cause) {
		super(cause);
	}
	
	

}
