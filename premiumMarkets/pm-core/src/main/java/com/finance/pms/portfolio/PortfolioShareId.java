package com.finance.pms.portfolio;

import javax.persistence.Id;

import com.finance.pms.datasources.shares.StockId;


public class PortfolioShareId extends StockId {
	
	/**
	 * 
	 */
	private static final long serialVersionUID = 1169434372679058467L;
	
	private String name;
	
	
	@SuppressWarnings("unused")
	private PortfolioShareId() {
	}
	

	public PortfolioShareId(String symbol, String isin, String portfolio) {
		super(symbol, isin);
		this.name = portfolio;
	}

	@Id
	public String getName() {
		return name;
	}

	public void setName(String portfolio) {
		this.name = portfolio;
	}

	@Override
	public int hashCode() {
		final int prime = 31;
		int result = super.hashCode();
		result = prime * result + ((name == null) ? 0 : name.hashCode());
		return result;
	}

	@Override
	public boolean equals(Object obj) {
		if (this == obj)
			return true;
		if (!super.equals(obj))
			return false;
		if (getClass() != obj.getClass())
			return false;
		final PortfolioShareId other = (PortfolioShareId) obj;
		if (name == null) {
			if (other.name != null)
				return false;
		} else if (!name.equals(other.name))
			return false;
		return true;
	}
	
}
